// Generates DefaultCatalog+Generated.swift from the canonical blocklist catalog.
//
// The canonical source of truth is lavasec-doc/data/blocklist-catalog.yml, published
// as blocklist-catalog.json. A copy is vendored at Catalog/blocklist-catalog.json
// (the "adopted" index). This script projects that JSON into the Swift DefaultCatalog
// the app compiles against — so the iOS catalog can never silently drift from the spec.
//
// Usage:
//   node scripts/generate-blocklist-catalog.mjs           # regenerate the Swift file
//   node scripts/generate-blocklist-catalog.mjs --check    # fail if the Swift file is stale (CI)
//
// To adopt a new catalog version: refresh Catalog/blocklist-catalog.json from
// lavasec-doc, then run this script and commit both.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const iosRoot = path.resolve(__dirname, "..");
const inputPath = path.join(iosRoot, "Catalog", "blocklist-catalog.json");
const outputPath = path.join(iosRoot, "Sources", "LavaSecKit", "Generated", "DefaultCatalog+Generated.swift");

// Stable Swift identifiers for each catalog id. The first 14 match names the app and

// Canonical category id -> Swift BlocklistCategory case.
const CATEGORY_CASES = {
  security: "security",
  multi_purpose: "multiPurpose",
  ads_tracking: "adsTracking",
  social: "social",
  nsfw: "nsfw",
  gambling: "gambling",
  piracy: "piracy",
};

// Canonical warning_level -> Swift WarningLevel case (identical spelling today).
const WARNING_CASES = { normal: "normal", advanced: "advanced", aggressive: "aggressive" };

function fail(message) {
  console.error(`generate-blocklist-catalog: ${message}`);
  process.exit(1);
}

function swiftStringLiteral(value) {
  return `"${value.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
}

function build() {
  if (!fs.existsSync(inputPath)) {
    fail(`missing canonical catalog at ${path.relative(iosRoot, inputPath)} — vendor it from lavasec-doc.`);
  }
  const doc = JSON.parse(fs.readFileSync(inputPath, "utf8"));
  if (doc.schema_version !== 1) fail(`unexpected schema_version ${doc.schema_version}`);

  const sources = doc.sources;
  const lines = [];
  lines.push("// Generated by scripts/generate-blocklist-catalog.mjs — DO NOT EDIT.");
  lines.push("// Source of truth: lavasec-doc/data/blocklist-catalog.yml (vendored at Catalog/blocklist-catalog.json).");
  lines.push("// Regenerate after adopting a new catalog version: node scripts/generate-blocklist-catalog.mjs");
  lines.push("");
  lines.push("import Foundation");
  lines.push("");
  lines.push("public extension DefaultCatalog {");

  for (const source of sources) {
    const name = SWIFT_NAMES[source.id];
    if (!name) fail(`no Swift identifier mapped for catalog id "${source.id}" — add it to SWIFT_NAMES.`);
    const category = CATEGORY_CASES[source.category];
    if (!category) fail(`unknown category "${source.category}" for "${source.id}".`);
    const warning = WARNING_CASES[source.warning_level];
    if (!warning) fail(`unknown warning_level "${source.warning_level}" for "${source.id}".`);

    // Defense in depth — the doc generator already enforces these, but never let a
    // bad vendored JSON compile a GPL default-on into the app.
    if (source.license === "GPL-3.0" && source.default_enabled) {
      fail(`"${source.id}" is GPL-3.0 and must not be default_enabled.`);
    }
    if (source.redistribution_mode !== "source_url_only") {
      fail(`"${source.id}" must be source_url_only.`);
    }

    lines.push(`    static let ${name} = BlocklistSource(`);
    lines.push(`        id: ${swiftStringLiteral(source.id)},`);
    lines.push(`        name: ${swiftStringLiteral(source.name)},`);
    lines.push(`        sourceURL: URL(string: ${swiftStringLiteral(source.source_url)})!,`);
    lines.push(`        licenseName: ${swiftStringLiteral(source.license)},`);
    lines.push(`        category: .${category},`);
    lines.push(`        defaultEnabled: ${source.default_enabled ? "true" : "false"},`);
    lines.push(`        warningLevel: .${warning}`);
    lines.push(`    )`);
    lines.push("");
  }

  lines.push("    /// Every curated source, in canonical catalog order (category order, then name).");
  lines.push("    /// This is the single bundled list the picker and onboarding build from.");
  lines.push("    static let curatedSources: [BlocklistSource] = [");
  for (const source of sources) {
    lines.push(`        ${SWIFT_NAMES[source.id]},`);
  }
  lines.push("    ]");
  lines.push("}");
  lines.push("");

  return lines.join("\n");
}

const generated = build();

if (process.argv.includes("--check")) {
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
  if (current !== generated) {
    fail(
      `${path.relative(iosRoot, outputPath)} is out of date with the canonical catalog.\n` +
        `Run: node scripts/generate-blocklist-catalog.mjs`
    );
  }
  console.log(`Blocklist catalog Swift is in sync (${JSON.parse(fs.readFileSync(inputPath, "utf8")).sources.length} sources).`);
} else {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, generated);
  console.log(`Wrote ${path.relative(iosRoot, outputPath)}.`);
}
